@implementation DOPayerPerf
@synthesize regionDO=_regionDO;
@synthesize t2=_t2;
@synthesize t3=_t3;
@synthesize rest=_rest;
@synthesize nc=_nc;
@synthesize payers=_payers;
@synthesize accessAct=_accessAct;
@synthesize accessPLN=_accessPLN;

-(id) initWithRegionDO:(DORegion *)regionDO t2:(NSNumber *)t2 t3:(NSNumber *)t3 rest:(NSNumber *)rest nc:(NSNumber *)nc payers:(NSArray *)payers accessAct:(NSNumber *)accessAct accessPLN:(NSNumber *)accessPLN;
{
    self = [super init];
    if(self)
    {
        self.regionDO = regionDO;
        
        self.t2 = t2;
        self.t3 = t3;
        self.rest = rest;
        self.nc = nc;
        self.payers = payers;
        self.accessPLN=accessPLN;
        self.accessAct=accessAct;
    }
    
    return self;
}

@end
