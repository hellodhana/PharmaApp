#import "BrandDO.h"

@implementation BrandDO
@synthesize brandID=_brandID;
@synthesize brandDesc=_brandDesc;

-(id)initWithBrandID:(NSNumber *)brandID brandDesc:(NSString *)desc
{
    self = [super init];
    if(self)
    {
        self.brandID = brandID;
        self.brandDesc = desc;
    }
    return self;
}
@end
